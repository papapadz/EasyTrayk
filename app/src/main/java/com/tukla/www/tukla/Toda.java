package com.tukla.www.tukla;

public class Toda {

    private String code;
    private String name;

    public Toda() {

    }

    public Toda(String code, String name) {
        this.code = code;
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }
}
